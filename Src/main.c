void HAL_TIM_PeriodElapsedCallback(TIM_HandleTypeDef *htim)
{
	if(htim->Instance==TIM3) //Fungsi Interrupt Timer
		{
			omega = 2.0*pi*f;
			vs=A*arm_sin_f32(omega*t); //ma*A 2047
			vs1=vs+A;
			vs2=A-vs; // 2048
			
			is=A*arm_sin_f32(omega*t - teta*RAD_PER_DEG);
			i2=A-is;
			
			t=t+(1/fs);
			
			if (t>=1/f) //1/f
				t=0;
			
			HAL_DAC_SetValue(&hdac,DAC_CHANNEL_1,DAC_ALIGN_12B_R,vs2);
			HAL_DAC_SetValue(&hdac,DAC_CHANNEL_2,DAC_ALIGN_12B_R,i2);
		}
}
